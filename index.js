const struct = type => cons => {
  const f = x => ({
    ["run" + type]: x,
    [TYPE]: type,
  });

  return cons(f);
};


const structMemo = type => cons => {
  const f = (thunk, args) => ({
    get ["run" + type] () {
      delete this["run" + type];
      console.log("eval");
      return this["run" + type] = thunk();
    },
    [TYPE]: type
  });

  return cons(f);
};


